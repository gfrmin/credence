#!/usr/bin/env python3
"""Terminal-Bench capability×cost matrix for the credence-pi routing proof.

For each model tier × task, run the `claude-code-stream-jsonl` agent (native
tool-calling agentic loop — the same class as OpenClaw) on a real Terminal-Bench
task, graded by the task's own deterministic test suite. Records, per run:

  - resolved   : tb's deterministic grade (is_resolved)
  - cost_usd   : claude's own total_cost_usd (the real bill, priced live by the
                 CLI — authoritative; avoids a stale local price table)
  - num_turns  : agentic turns (feeds the Poisson-Gamma turns belief)
  - tokens     : full cache-aware breakdown (input/output/cache_read/cache_create)
  - modelUsage : per-model token+cost breakdown (transparency; multi-model runs)
  - duration_s, failure_mode, is_error

This is the empirical ground truth the Bayesian router is a decision OVER: it
captures the capability×cost spread (a cheap model that thrashes on a hard task
burns more turns/tokens and can cost MORE than a dearer model that one-shots it).

The matrix is generated by tb's native --n-concurrent; we run one `tb run` per
tier (each fans out over all tasks). No arbitrary constants: cost is claude's
realized bill, correctness is the task's test suite.

Usage:
    python3 tb_matrix.py run  --tiers haiku,sonnet,opus --tasks hello-world,fix-permissions
    python3 tb_matrix.py parse /tmp/tb-runs/<run-id> --tier haiku   # re-parse only
"""
from __future__ import annotations

import argparse
import json
import os
import re
import subprocess
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent
RESULTS_DIR = ROOT / "results"
TB_BIN = "/tmp/tb-tool/bin/tb"
DATASET = "terminal-bench-core==0.1.1"
DATASET_DIR = Path.home() / ".cache/terminal-bench/terminal-bench-core/0.1.1"
DOCKER_HOST = "unix:///run/user/1000/podman/podman.sock"
RUNS_OUT = "/tmp/tb-runs"

# Model ref per tier (the arm). Local (ollama) is handled by OpenClaw, not the
# claude CLI, so it is excluded from this matrix; the Anthropic ladder is the
# capability×cost spine.
TIER_MODEL = {
    "haiku": "anthropic/claude-haiku-4-5-20251001",
    "sonnet": "anthropic/claude-sonnet-4-6",
    "opus": "anthropic/claude-opus-4-8",
}

# Published prices, USD/MTok (input, output, cache_read, 5m-cache_write),
# verified 2026-06-17 against platform.claude.com/docs/.../pricing. We report
# claude's own total_cost_usd as the headline cost (the real bill); these rates
# are kept for transparency, for the brain's E[cost] belief, and as a cross-check
# that reproduces total_cost_usd exactly. Opus 4.8 = $5/$25 (NOT old $15/$75).
PRICES = {
    "haiku": dict(i=1.0, o=5.0, cr=0.10, cw=1.25),
    "sonnet": dict(i=3.0, o=15.0, cr=0.30, cw=3.75),
    "opus": dict(i=5.0, o=25.0, cr=0.50, cw=6.25),
}


def price_from_tokens(tier: str, inp: int, out: int, cr: int, cw: int) -> float:
    """Cost from a token breakdown at published rates — cross-checks total_cost_usd."""
    p = PRICES[tier]
    return (inp * p["i"] + out * p["o"] + cr * p["cr"] + cw * p["cw"]) / 1e6


def load_key() -> str:
    out = subprocess.run(
        ["secret-tool", "lookup", "service", "env", "key", "ANTHROPIC_API_KEY"],
        capture_output=True, text=True)
    k = out.stdout.strip()
    if not k:
        sys.exit("ANTHROPIC_API_KEY not found in keyring")
    return k


def _tier_of(model_id: str) -> str:
    m = (model_id or "").lower()
    for t in ("haiku", "sonnet", "opus"):
        if t in m:
            return t
    return "other"


def parse_stream(stream_path: Path) -> dict:
    """Extract cost/turns/tokens from a claude --output-format stream-json file.

    Authoritative source is the final `result` event (cumulative usage,
    num_turns, total_cost_usd). When it is ABSENT (timeout / killed mid-run —
    exactly the thrashing case, which must NOT read as $0), recover token totals
    from the per-message `assistant` events: each message.id is re-emitted 2-3×
    during streaming, so dedup by id taking the max-output occurrence (input and
    cache tokens are request-time-fixed; only output grows). The recovered total
    is a tight LOWER BOUND — the final message may have been truncated mid-output,
    but input+cache (which dominate a thrash) are exact. parse_run prices the
    recovered tokens at the verified per-tier rate and flags cost_lower_bound.
    """
    out = dict(
        complete=False, cost_usd=None, num_turns=None, duration_s=None,
        is_error=None, stop_reason=None,
        input=0, output=0, cache_read=0, cache_create=0,
        model_usage={}, primary_model=None,
    )
    if not stream_path.exists():
        return out
    result = None
    by_id: dict[str, dict] = {}            # message.id -> usage with max output
    assistant_models: dict[str, int] = {}
    for ln in stream_path.read_text().splitlines():
        ln = ln.strip()
        if not ln:
            continue
        try:
            r = json.loads(ln)
        except json.JSONDecodeError:
            continue
        if r.get("type") == "assistant":
            m = r.get("message") or {}
            mid, u = m.get("id"), m.get("usage") or {}
            if m.get("model"):
                assistant_models[m["model"]] = assistant_models.get(m["model"], 0) + 1
            if mid is not None:
                prev = by_id.get(mid)
                if prev is None or (u.get("output_tokens", 0) or 0) > (prev.get("output_tokens", 0) or 0):
                    by_id[mid] = u
        elif r.get("type") == "result":
            result = r
    if result is not None:
        u = result.get("usage") or {}
        mu = result.get("modelUsage") or {}
        out.update(
            complete=True,
            cost_usd=result.get("total_cost_usd"),
            num_turns=result.get("num_turns"),
            duration_s=round((result.get("duration_ms") or 0) / 1000.0, 1),
            is_error=result.get("is_error"),
            stop_reason=result.get("stop_reason") or result.get("terminal_reason"),
            input=u.get("input_tokens", 0) or 0,
            output=u.get("output_tokens", 0) or 0,
            cache_read=u.get("cache_read_input_tokens", 0) or 0,
            cache_create=u.get("cache_creation_input_tokens", 0) or 0,
            model_usage=mu,
        )
        if mu:
            out["primary_model"] = max(
                mu, key=lambda k: (mu[k].get("inputTokens", 0) + mu[k].get("outputTokens", 0)))
        return out
    # No result event — recover token totals from the deduped assistant messages.
    if by_id:
        out["input"] = sum(u.get("input_tokens", 0) or 0 for u in by_id.values())
        out["output"] = sum(u.get("output_tokens", 0) or 0 for u in by_id.values())
        out["cache_read"] = sum(u.get("cache_read_input_tokens", 0) or 0 for u in by_id.values())
        out["cache_create"] = sum(u.get("cache_creation_input_tokens", 0) or 0 for u in by_id.values())
        out["num_turns"] = len(by_id)
    if assistant_models:
        out["primary_model"] = max(assistant_models, key=assistant_models.get)
    return out


def task_features(task_id: str) -> dict:
    """Context features X for a task, from its task.yaml — the router's observables.

    difficulty/category are the human-assigned correlates of hardness; instr_len is
    a prompt-only feature (used to refute 'you fed the label in as a feature').
    """
    f = DATASET_DIR / task_id / "task.yaml"
    out = dict(difficulty=None, category=None, n_tags=0, instr_len=0,
               max_agent_timeout=None)
    if not f.exists():
        return out
    txt = f.read_text()
    m = re.search(r"(?m)^difficulty:\s*(\S+)", txt)
    out["difficulty"] = m.group(1) if m else None
    m = re.search(r"(?m)^category:\s*(\S+)", txt)
    out["category"] = m.group(1) if m else None
    m = re.search(r"(?m)^max_agent_timeout_sec:\s*([0-9.]+)", txt)
    out["max_agent_timeout"] = float(m.group(1)) if m else None
    # Instruction block: 'instruction: |-' (or '|') up to the next top-level key.
    im = re.search(r"(?ms)^instruction:\s*\|-?\s*\n(.*?)(?=^\S)", txt)
    out["instr_len"] = len(im.group(1)) if im else 0
    # tags: count list items between 'tags:' and the next top-level key.
    tm = re.search(r"(?ms)^tags:\s*\n(.*?)(?=^\S)", txt)
    out["n_tags"] = len(re.findall(r"(?m)^\s*-\s+\S", tm.group(1))) if tm else 0
    return out


def parse_run(run_dir: Path, tier: str) -> list[dict]:
    """Join tb's per-trial grade with the claude stream cost for every task."""
    top = run_dir / "results.json"
    if not top.exists():
        print(f"  !! no results.json in {run_dir}", file=sys.stderr)
        return []
    summary = json.loads(top.read_text())
    rows = []
    for res in summary.get("results", []):
        task_id = res["task_id"]
        trial_name = res["trial_name"]
        trial_dir = run_dir / task_id / trial_name
        stream = trial_dir / "sessions" / "claude_stream.jsonl"
        s = parse_stream(stream)
        feats = task_features(task_id)
        # Cost: claude's own total_cost_usd when the run completed (authoritative);
        # else the verified-rate price of the recovered tokens (a lower bound — the
        # thrash/timeout case must not read as $0).
        if s["cost_usd"] is not None:
            cost, lower = s["cost_usd"], False
        elif (s["input"] or s["output"] or s["cache_read"] or s["cache_create"]):
            cost = price_from_tokens(tier, s["input"], s["output"],
                                     s["cache_read"], s["cache_create"])
            lower = True
        else:
            cost, lower = None, False
        rep_m = re.search(r"\.(\d+)-of-\d+\.", trial_name)
        rows.append(dict(
            task=task_id,
            tier=tier,
            rep=int(rep_m.group(1)) if rep_m else 1,
            resolved=bool(res.get("is_resolved")),
            failure_mode=res.get("failure_mode"),
            difficulty=feats["difficulty"],
            category=feats["category"],
            instr_len=feats["instr_len"],
            n_tags=feats["n_tags"],
            cost_usd=cost,
            cost_lower_bound=lower,
            num_turns=s["num_turns"],
            duration_s=s["duration_s"],
            input=s["input"], output=s["output"],
            cache_read=s["cache_read"], cache_create=s["cache_create"],
            primary_model=s["primary_model"],
            stream_complete=s["complete"],
            agent_is_error=s["is_error"],
            stop_reason=s["stop_reason"],
            model_usage=s["model_usage"],
        ))
    return rows


def run_arm(tier: str, tasks: list[str], n_concurrent: int, key: str,
            timeout_mult: float, label: str, n_attempts: int = 1) -> Path:
    model = TIER_MODEL[tier]
    env = dict(os.environ)
    env["ANTHROPIC_API_KEY"] = key
    env["DOCKER_HOST"] = DOCKER_HOST
    # Make tb_agent importable. Join WITHOUT a trailing colon — a trailing/empty
    # PYTHONPATH entry means "add cwd", which would let a stray module in the cwd
    # shadow a real package (e.g. /tmp/runs.py shadowing the `runs` dependency).
    existing = env.get("PYTHONPATH", "")
    env["PYTHONPATH"] = (str(ROOT) + os.pathsep + existing) if existing else str(ROOT)
    run_id = f"matrix-{label}-{tier}"
    # Fresh dir each invocation: a stale tb.lock from a prior run with different
    # tasks makes tb try (and refuse) to resume. Re-runnable by construction.
    run_dir = Path(RUNS_OUT) / run_id
    if run_dir.exists():
        import shutil
        shutil.rmtree(run_dir, ignore_errors=True)
    cmd = [
        TB_BIN, "run",
        "--dataset", DATASET,
        "--agent-import-path", "tb_agent:ClaudeCodeStreamJSONL",
        "--model", model,
        "--output-path", RUNS_OUT,
        "--run-id", run_id,
        "--n-concurrent", str(n_concurrent),
        "--n-attempts", str(n_attempts),
        "--global-timeout-multiplier", str(timeout_mult),
        "--no-cleanup",
    ]
    for t in tasks:
        cmd += ["--task-id", t]
    print(f"\n=== ARM {tier} ({model}) over {len(tasks)} tasks, "
          f"n_concurrent={n_concurrent} ===", flush=True)
    subprocess.run(cmd, env=env, cwd=str(ROOT))
    return Path(RUNS_OUT) / run_id


def summarize(rows: list[dict]) -> None:
    tiers = sorted({r["tier"] for r in rows})
    print("\n=== MATRIX SUMMARY (per tier) ===")
    for t in tiers:
        rs = [r for r in rows if r["tier"] == t]
        n = len(rs)
        solved = sum(1 for r in rs if r["resolved"])
        costs = [r["cost_usd"] for r in rs if r["cost_usd"] is not None]
        total = sum(costs)
        print(f"  {t:>7}: {solved}/{n} solved | ${total:.3f} total | "
              f"${total/n:.4f} mean/run | "
              f"${total/solved:.4f} per-solve" if solved else
              f"  {t:>7}: {solved}/{n} solved | ${total:.3f} total")


def main() -> None:
    ap = argparse.ArgumentParser()
    sub = ap.add_subparsers(dest="cmd", required=True)

    pr = sub.add_parser("run")
    pr.add_argument("--tiers", default="haiku,sonnet,opus")
    pr.add_argument("--tasks", required=True, help="comma list of task ids")
    pr.add_argument("--label", default="run", help="run-id label (pilot/full/...)")
    pr.add_argument("--reps", type=int, default=1, help="attempts per task (tb --n-attempts)")
    pr.add_argument("--n-concurrent", type=int, default=4)
    pr.add_argument("--timeout-mult", type=float, default=1.0)
    pr.add_argument("--out", default=str(RESULTS_DIR / "tb_matrix.jsonl"))

    pp = sub.add_parser("parse")
    pp.add_argument("run_dir")
    pp.add_argument("--tier", required=True)
    pp.add_argument("--out", default=str(RESULTS_DIR / "tb_matrix.jsonl"))

    args = ap.parse_args()
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)

    if args.cmd == "parse":
        rows = parse_run(Path(args.run_dir), args.tier)
        _emit(rows, args.out, append=True)
        return

    key = load_key()
    tiers = [t for t in args.tiers.split(",") if t]
    tasks = [t for t in args.tasks.split(",") if t]
    for t in tiers:
        if t not in TIER_MODEL:
            sys.exit(f"unknown tier {t}; valid: {list(TIER_MODEL)}")

    all_rows: list[dict] = []
    for tier in tiers:
        run_dir = run_arm(tier, tasks, args.n_concurrent, key, args.timeout_mult,
                          args.label, args.reps)
        rows = parse_run(run_dir, tier)
        all_rows.extend(rows)
        for r in rows:
            print(f"  [{tier:>7}] {r['task']:<34} "
                  f"{'OK ' if r['resolved'] else 'FAIL'} "
                  f"${(r['cost_usd'] or 0):.4f} {r['num_turns']}t "
                  f"{r['duration_s']}s {r['failure_mode']}")
    _emit(all_rows, args.out, append=False)
    summarize(all_rows)
    print(f"\nrows -> {args.out}")


def _emit(rows: list[dict], out: str, append: bool) -> None:
    mode = "a" if append else "w"
    with open(out, mode) as fh:
        for r in rows:
            fh.write(json.dumps(r) + "\n")
    print(f"wrote {len(rows)} rows -> {out} (mode={mode})")


if __name__ == "__main__":
    main()
